package it.polimi.ingsw.model;

/**
 * <h1>Enumeration SpaceType</h1>
 * The enumeration SpaceType represents the type of a space of the living room board
 * It can be playable or not (forbidden). The playable spaces change according to the number of players
 *
 * @author Francesca Pia Panaccione, Francesco Santambrogio
 * @version 1.0
 * @since 4/8/2023
 */
public enum SpaceType {
    PLAYABLE,
    FORBIDDEN
}
